let x = 1, y = 5;

/* Quando utilizamos o && os dois argumentos precisam ser verdadeiros para entrar dentro do IF */

if(x == 10 && y == 5){
    console.log(true);
}else{
    console.log(false);
}

/* Quando utilizamos o || pelo menos um dos argumentos precisa ser verdadeiro para entrar dentro do IF */

if(x == 10 || y == 5){
    console.log(true);
}else{
    console.log(false);
}
